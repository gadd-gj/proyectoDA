    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed

        try {

            Empleado empleado = new Empleado();

            empleado.setNombre(txtNombre.getText());
            empleado.setDireccion(txtDireccion.getText());
            empleado.setTelefono(txtTelefono.getText());
            empleado.setIdDepartamento(Long.parseLong(txtDepartamento.getText()));
            

            
            empleadoDao.create(empleado);

            JOptionPane.showMessageDialog(null, "Empleado creado correctamente \n" + empleado.toString());

            limpiarTextFields();
            cargar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getLocalizedMessage());
            limpiarTextFields();
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {

            Empleado empleado = empleadoDao.getEmpleado(txtId.getText());
            JOptionPane.showMessageDialog(null, "Empleado encontrado \n" + empleado.toString());

            limpiarTextFields();
            cargar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e.getMessage());
            limpiarTextFields();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try {

            Empleado empleado = new Empleado();
            empleado.setId(Long.parseLong(txtId.getText()));
            empleado.setNombre(txtNombre.getText());
            empleado.setDireccion(txtDireccion.getText());
            empleado.setTelefono(txtTelefono.getText());
            empleado.setIdDepartamento(Long.parseLong(txtDepartamento.getText()));
            
            empleadoDao.update(empleado);

            JOptionPane.showMessageDialog(null, "Empleado actualizado correctamente");

            limpiarTextFields();
            cargar();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error : " + e.getMessage());
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

