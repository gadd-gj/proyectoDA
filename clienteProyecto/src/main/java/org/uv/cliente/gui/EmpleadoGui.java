    private void cargar() {
        limpiar();
        table = (DefaultTableModel) tablaEmp.getModel();
        List<Empleado> list = empleadoDao.obtenerTodo();
        Object[] filas = new Object[5];
        
        for (Empleado listEmp : list) {
            filas[0] = listEmp.getId();
            filas[1] = listEmp.getNombre();
            filas[2] = listEmp.getDireccion();
            filas[3] = listEmp.getTelefono();
            filas[4] = listEmp.getDepartamento().getIdDepartamento();
            
            table.addRow(filas);
        }
    }

    private void limpiar() {
        for (int i = 0; i < tablaEmp.getRowCount(); i++) {
            table.removeRow(i);
            i -= 1;
        }
    }

    private void limpiarTextFields() {
        txtId.setText("");
        txtNombre.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        txtDepartamento.setText("");
    }


